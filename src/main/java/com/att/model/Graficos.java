package com.att.model;

public class Graficos {

	private int analisis;
	private int construccion;
	private int pruebas;
	private int liberacion;
	private int cierre;
	private int totalInc;
	private int totalIncSel;

	public int getAnalisis() {
		return analisis;
	}

	public void setAnalisis(int analisis) {
		this.analisis = analisis;
	}

	public int getConstruccion() {
		return construccion;
	}

	public void setConstruccion(int construccion) {
		this.construccion = construccion;
	}

	public int getPruebas() {
		return pruebas;
	}

	public void setPruebas(int pruebas) {
		this.pruebas = pruebas;
	}

	public int getLiberacion() {
		return liberacion;
	}

	public void setLiberacion(int liberacion) {
		this.liberacion = liberacion;
	}

	public int getCierre() {
		return cierre;
	}

	public void setCierre(int cierre) {
		this.cierre = cierre;
	}

	public int getTotalInc() {
		return totalInc;
	}

	public void setTotalInc(int totalInc) {
		this.totalInc = totalInc;
	}

	public int getTotalIncSel() {
		return totalIncSel;
	}

	public void setTotalIncSel(int totalIncSel) {
		this.totalIncSel = totalIncSel;
	}

}
